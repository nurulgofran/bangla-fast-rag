def generate_products(num_products: int = 5000) -> list[dict]:
    """Generate synthetic Bangla product dataset with UNIQUE names."""
    products = []
    used_names: set[str] = set()
    product_id = 1
    categories = list(CATALOG.keys())

    while len(products) < num_products:
        category = random.choice(categories)
        base_name, price_min, price_max, desc_template = random.choice(CATALOG[category])

        brand = random.choice(BRANDS)
        quality = random.choice(QUALITY_TAGS)
        price = random.randint(price_min, price_max)

        name_variant = random.choice([
            f"{brand} {base_name}",
            f"{base_name} ({quality})",
            f"{brand} {base_name} - {quality}",
            f"{base_name} {quality}",
        ])

        if name_variant in used_names:
            continue
        used_names.add(name_variant)

        description = desc_template.format(name=base_name)

        # Add multiple extra sentences to reach ~5MB total text
        extra_sentences = [
            f" {brand} ব্র্যান্ডের গুণগত মানের পণ্য।",
            f" দ্রুত ডেলিভারি পাওয়া যায়, সারা বাংলাদেশে হোম ডেলিভারি সুবিধা।",
            f" সীমিত সময়ের জন্য বিশেষ মূল্য ছাড় চলছে, এখনই অর্ডার করুন।",
            f" সর্বোচ্চ গ্রাহক সন্তুষ্টি নিশ্চিত, রিটার্ন পলিসি প্রযোজ্য।",
            f" ১০০% অরিজিনাল পণ্য, নকলের বিরুদ্ধে গ্যারান্টি।",
            f" এই পণ্যটি বাংলাদেশের বাজারে অত্যন্ত জনপ্রিয় এবং বহুল ব্যবহৃত।",
            f" {quality} কোয়ালিটির এই পণ্যটি দীর্ঘস্থায়ী এবং টেকসই উপাদান দিয়ে তৈরি।",
            f" বিশেষ অফার: একসাথে দুটি কিনলে অতিরিক্ত ১০% ছাড় পাবেন।",
            f" এই পণ্যের {brand} ওয়ারেন্টি রয়েছে, ক্রয়ের পর সার্ভিস সুবিধা পাবেন।",
            f" গ্রাহকদের রিভিউ অনুযায়ী এটি ৪.৫ স্টার রেটিং পেয়েছে।",
            f" পরিবেশবান্ধব প্যাকেজিংয়ে পাঠানো হয়, নিরাপদ ও সুরক্ষিত।",
            f" ক্যাশ অন ডেলিভারি, বিকাশ, নগদ ও কার্ডে পেমেন্ট গ্রহণযোগ্য।",
            f" {category} বিভাগে এটি সবচেয়ে বেশি বিক্রিত পণ্যগুলোর একটি।",
            f" মানসম্পন্ন কাঁচামাল ব্যবহার করে তৈরি, আন্তর্জাতিক মান বজায় রাখা হয়েছে।",
            f" প্রতিটি পণ্য কঠোর মান নিয়ন্ত্রণ প্রক্রিয়ার মধ্য দিয়ে যায়।",
            f" বিশেষ উপলক্ষে উপহার হিসেবে দিতে পারেন, গিফট র্যাপিং সুবিধা আছে।",
        ]
        # Each product gets 5-6 extra sentences to reach ~5MB total
        num_extra = random.randint(5, 6)
        selected = random.sample(extra_sentences, num_extra)
        description += "".join(selected)

        product = {
            "id": product_id,
            "name_bn": name_variant,
            "category_bn": category,
            "price_bdt": price,
            "description_bn": description,
            "brand_bn": brand,
        }
        products.append(product)
        product_id += 1

    return products[:num_products]


def save_dataset(products: list[dict], output_path: Path) -> None:
    """Save products to JSON file."""
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(products, f, ensure_ascii=False, indent=2)

    total_text = ""
    for p in products:
        total_text += f"{p['name_bn']} {p['category_bn']} {p['description_bn']} মূল্য: {p['price_bdt']} টাকা\n"

    size_bytes = len(total_text.encode("utf-8"))
    print(f"Generated {len(products)} products")
    print(f"Saved to: {output_path}")
    print(f"Text data size: {size_bytes / 1024 / 1024:.2f} MB")
    print(f"JSON file size: {output_path.stat().st_size / 1024 / 1024:.2f} MB")


